    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed

        if (table.getSelectedRow() >= 0){
            int i = table.getSelectedRow();
            this.gerIG.getGerDominio().excluirProduto(this.produtos.get(i));
            try {
                this.loadTable();
            } catch (ParseException ex) {
                Logger.getLogger(DlgGerenciarCatProd.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }    
    }//GEN-LAST:event_excluirActionPerformed

    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed

        if (table.getSelectedRow() >= 0){
            int i = table.getSelectedRow();
            this.setVisible(false);
            this.gerIG.openJanelaCadastrarProduto(null, this.produtos.get(i));
            this.gerIG.atualizarTelas();

        }else{
            JOptionPane.showMessageDialog(null, "Favor selecionar uma linha");
        }  
    }//GEN-LAST:event_editarActionPerformed

